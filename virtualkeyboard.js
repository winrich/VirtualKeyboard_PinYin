/**
 * 点击删除事件
 * */
function backspace(type) {
  var key = $('.fullKeyboard .key');
  
  if (key.text()) {
    key.text(key.text().substring(0, key.text().length - 1));
    
    updateVal();
    
    if (!key.text()) {
      emptyZH();
    }
  }
  else {
    // var focusEl = $(keyboard.setting.el);
    if (type == "vkletter") {
      var num = check4ByteHZ($(".vktext").val().substr(-1)) ? 2 : 1;  //xh-20230306 解决4字节汉字显示乱码问题,4字节汉字被认为是2个字符
      
      $(".vktext").val($(".vktext").val().substring(0, $(".vktext").val().length - num));
    }
    else if (type == "vknum") {
      $(".numtext").val($(".numtext").val().substring(0, $(".numtext").val().length - 1));
    }
  }
}

function inputSpace(e) {
  if (keyboard.setting.ZH) {
    //$('.outputZone').show();	//huiyz-20230303：不隐藏中文备选区域
    var inputVal = $('.fullKeyboard .key');
    inputVal.text(inputVal.text() + ' ');
    updateVal(e);
  }
  else {
    var focusEl = $(keyboard.setting.el);
    focusEl.val(focusEl.val() + ' ');
  }
}

