/**
 * 更新当前候选页面值
 * */
function pageUpdate(count) {
  var pageText = keyboard.setting.page + '/' + (Math.ceil(count / keyboard.setting.num) || '1');
  $('.fullKeyboard .page').text(pageText);
  $('.fullKeyboard .panel-page-info').text(pageText);
}

function down(e) {
  e.preventDefault();
  var e = e.type.match('mouse') ? e : e.originalEvent.changedTouches[0]
  keyboard.setting.offsetX = (e.pageX - $('.fullKeyboard')[0].offsetLeft);
  keyboard.setting.offsetY = (e.pageY - $('.fullKeyboard')[0].offsetTop);
  $('html,body').on('mousemove', move);
  $('html,body').on('mouseup', up);
  $('html,body').on('touchmove', move);
  $('html,body').on('touchend', up);
}

function downNum(e) {
  e.preventDefault();
  var e = e.type.match('mouse') ? e : e.originalEvent.changedTouches[0]
  keyboard.setting.offsetX = (e.pageX - $('.numKeyboard')[0].offsetLeft);
  keyboard.setting.offsetY = (e.pageY - $('.numKeyboard')[0].offsetTop);
  $('html,body').on('mousemove', move);
  $('html,body').on('mouseup', up);
  $('html,body').on('touchmove', move);
  $('html,body').on('touchend', up);
}

function move(e) {
  e.preventDefault();
  var e = e.type.match('mouse') ? e : e.originalEvent.changedTouches[0];
  if ($('.fullKeyboard')[0] != undefined) {
    $('.fullKeyboard')[0].style.left = (e.pageX - keyboard.setting.offsetX) + 'px';
    $('.fullKeyboard')[0].style.top = (e.pageY - keyboard.setting.offsetY) + 'px';
  }
  else if ($('.numKeyboard')[0] != undefined) {
    $('.numKeyboard')[0].style.left = (e.pageX - keyboard.setting.offsetX) + 'px';
    $('.numKeyboard')[0].style.top = (e.pageY - keyboard.setting.offsetY) + 'px';
  }
}

function up() {
  $('html,body').off('mousemove', move);
  $('html,body').off('mouseup', up);
  $('html,body').off('touchmove', move);
  $('html,body').off('touchend', up);
}

/**
 * 关闭中文键盘
 * @param  entry  是否录入已上屏内容
 * @param cbFun 回调函数
 * */
function closeVK(entry, cbFun) {
  let inputValue = "";
  
  if (entry == true)	//录入已上屏内容
  {
    inputValue = $(".vktext").val();
    
    //$(keyboard.setting.el).val(inputValue);
    //inputEvent($(keyboard.setting.el).attr("id"));
    setInputValueAndSync($(keyboard.setting.el), inputValue)
  }
  
  // keyboard.setting.ZH = false;
  
  $('.virtualkeyboard').remove();
  
  topPage().hideMaskLayer();
  
  //huiyz-20221014：关闭键盘时添加回调功能
  if (cbFun != null && cbFun != undefined && typeof cbFun === "function") {
    cbFun(inputValue); //回调
  }
}

