/**
 * 判断输入字符是否为宽字符（4字节）
 * xh-20230306 为了解决4字节汉字显示乱码问题
 * */
function check4ByteHZ(str) {
  const testCases = ['\ud800', '\udbff', '\udc00', '\udfff'];   //宽字符高位代理对
  if ((str >= testCases[0] && str <= testCases[1]) || (str >= testCases[2] && str <= testCases[3])) { return true; }
  else { return false; }
}

/**
 * 根据键入内容更新候选区域
 * */
function updateVal() {
  $('.fullKeyboard .word').remove();  //移除所有候选字
  
  var inputVal = $('.fullKeyboard .key').text()
  if (!inputVal) { return }
  
  var count = 0;
  
  keyboard.setting.key.forEach(function (val, index) {
    //console.log(111, val);
    //console.log(222, inputVal);
    if (val.indexOf(inputVal) == 0) {
      for (var i = 0; i < dictionary[val].length; i++) {
        count++
        
        var hide = count > keyboard.setting.num ? ' vkHide' : ''; //huiyz-20220928：解决候选区域第一页和后面页面显示候选字数不一致的问题
        
        //xh-20230306 解决4字节汉字显示乱码问题，如“𫐓”rou是双字符汉字
        var hz = "";
        if (check4ByteHZ(dictionary[val][i])) {
          //4字节汉字被认为是2个字符，因此需要拼接为1个汉字
          hz = dictionary[val][i] + dictionary[val][i + 1];
          i++;
        }
        else { hz = dictionary[val][i]; }
        
        $('<li class="word' + hide + '">').appendTo('.output-ZH').text(hz);
      }
    }
  });
  
  //更新翻页按键
  if (count < keyboard.setting.num) {
    $('.fullKeyboard .outputZoneDown').addClass('unclick')
  }
  else {
    $('.fullKeyboard .outputZoneDown').removeClass('unclick')
  }
  
  keyboard.setting.page = 1;
  
  $('.fullKeyboard .outputZoneUp').addClass('unclick')
  
  pageUpdate(count) //更新当前候选页面值
}

